# data type check : type
#examples:
# string
name = "rafi"
print(name, type(name))
#int
x = 1
print(x, type(x))
#float
y = 10.5
print("float", y, type(y))
# list
name= ["hablu","boltu","noltu"]
print("list", name, type(name))
# dict
info = {"name" : "hablu", "age" : 16}
print("dict", info, type(info))
